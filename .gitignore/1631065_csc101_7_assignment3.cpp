#include<iostream>

using namespace std;

int main()
{



    int a = 1;
    int b = 2;

    cout<<"Enter The First Number: ";
    cin>>a;

    cout<<endl;

    cout<<"Enter The Second Number: ";
    cin>>b;

    cout<<endl;

    cout << a << " + " << b << " = " << a + b <<"\n";
    cout << a << " - " << b << " = " << a - b<<"\n";
    cout << a << " * " << b << " = " << a * b<<"\n";
    cout << a << " / " << b << " = " << a / b<<"\n";
    return 0;
}




